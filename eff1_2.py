#《Python Enhancement Proposal #8》（8号Python增强提案）又叫PEP8,它是针对Python代码格式而编订的风格指南。

#当编写Python代码时，总是应该遵循PEP8风格指南。与广大Python开发者采用同一套代码风格，可以使项目利于多人协作。

#Pylint(http://www.pylint.org/)是以款流行的Python源码静态分析工具。它可以自动检查受测代码是否符合PEP8风格指南，而且还能找出Python程序里的多种常见错误。

